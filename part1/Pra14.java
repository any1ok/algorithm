package part1;

import java.util.Scanner;

public class Pra14 {

	public static void main(String[] args) {
		int n;
		Scanner scan = new Scanner(System.in);
		n= scan.nextInt();
		for(int i=0;i<n;i++) {
			for(int j =0 ;j<n;j++) {
				System.out.print("*");
				
			}
			System.out.print("\n");
		}
		

	}

}
